/*
    INHERITANCE
    ====================================================================================
    Terminology

    - Inheritance
        - Process of creating new class from existring classes
        - Reuse mechanism

    - Single Inheritance
        - A new class is created from another 'single' class

    - Multiple Inheritance
        - A new class is created from two (or more) other classes

    ====================================================================================
    Terminology
                                                                        ============
    - Base class (parent class, super class)                            |   BASE   |
        - The class being extended or inherited from                    ============
                                                                             /\
    - Derived class (chiled class, sub class)                                ||
        - The class being created from the Base class                   ============
        - Will inherit attributes and operations from Base class        |  DERIVED |
                                                                        ============
    ====================================================================================
    Terminology

    - "Is-A" relationship
        - Public inheritance
        - Derived classes are sub-types of their Base classes
        - Can use derived class object wherever we use a base class object

    - Generalization
        - Combining similar classes into a single, more general class based on
          common attributes
    
    - Specialization
        - Creating new class from existring classes proving more specialized attributes
          or operations

    - Inheritance or Class Hierarchies
        - Organization of our inheritance relationships
    ====================================================================================
    

    
*/